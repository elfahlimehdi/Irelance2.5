// Fonction pour obtenir une image aléatoire d'une catégorie
export const getRandomProductImage = (category: string, productType: string): string => {
  const categoryImages = productImagesDatabase[category as keyof typeof productImagesDatabase];
  if (!categoryImages || !categoryImages[productType as keyof typeof categoryImages]) {
    return "https://images.unsplash.com/photo-1560472354-b33ff0c44a43?w=800&q=80"; // Image par défaut
  }
  
  const images = categoryImages[productType as keyof typeof categoryImages];
  if (Array.isArray(images) && images.length > 0) {
    return images[Math.floor(Math.random() * images.length)];
  }
  return "https://images.unsplash.com/photo-1560472354-b33ff0c44a43?w=800&q=80"; // Image par défaut
};

